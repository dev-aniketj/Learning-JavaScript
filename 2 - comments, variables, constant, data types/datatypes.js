// String
let message = "my name is aniket";
console.log(message);
console.log(typeof message);

// Number
let age = 23;
let pi = 3.14;
console.log(age);
console.log(pi);
console.log(typeof age, typeof pi);

// Boolean
flag = true;
console.log(flag);
console.log(typeof flag);

// Undefined
let a;
let b;
console.log("a = ", a);
console.log("b = ", b);
console.log(typeof a, typeof b);

// Null
let x = null;
console.log("x = ", x);
console.log(typeof x);
