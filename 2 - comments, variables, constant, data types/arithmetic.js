var num1 = 8;
var num2 = 3;

// addition, subtraction, multiplication, divide
var sum = num1 + num2;
var sub = num1 - num2;
var mul = num1 * num2;
var div = num1 / num2;

console.log(num1, num2);
console.log(sum);
console.log(sub);
console.log(mul);
console.log(div);

// remainder
var rem = num1 % num2;
console.log("remainder : ", rem);

// increment, decrement
var a = 5;
a++; //6
console.log("a : ", a); //6
console.log("a : ", ++a); //7

var b = 3;
b--; //2
console.log("b : ", b--); //1
console.log("b : ", --b); //0

// exponentiation (**)
var num = 2;
console.log(num ** 8);
console.log(3 ** 3);
console.log(4 ** num);
