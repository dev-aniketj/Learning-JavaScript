// <, <=, >, >=, ==, !=
// === (equal value and equal type)
// !== (not equal value and not equal type)

let age = 23;

if (age > 18) {
  console.log("Age is greater than 18.");
} else {
  console.log("Age is not greater than 18.");
}

let AGE = "10";
if (age === AGE) {
  console.log("age is equal to AGE");
} else {
  console.log("age is not equal to AGE");
}
