function sum(a, b) {
  console.log(`Sum of ${a} and ${b} is ${a + b}`);
}
function sub(a, b) {
  console.log(`Subraction from ${a} to ${b} is ${a - b}`);
}
function multiple(a, b) {
  console.log(`Multiplication of ${a} and ${b} is ${a * b}`);
}
function divide(a, b) {
  console.log(`Divide of ${a} and ${b} is ${a / b}`);
}

sum(3, 5);
sub(10, 5);
multiple(4, 3);
divide(10, 3);
